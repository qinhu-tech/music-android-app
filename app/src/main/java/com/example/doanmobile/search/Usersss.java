package com.example.doanmobile.search;

public class Usersss {
    private int image;
    private String namess;
    private String address;

    public Usersss(int image, String namess, String address) {
        this.image = image;
        this.namess = namess;
        this.address = address;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getNamess() {
        return namess;
    }

    public void setNamess(String namess) {
        this.namess = namess;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }
}
